def build_category_index() -> Dict[int, Dict]:
    return {category["id"]: category for category in CATEGORIES}


CATEGORY_BY_ID = build_category_index()


def category_lookup(user_input: str) -> Optional[Dict]:
    cleaned = normalize(user_input)

    if not cleaned:
        return None

    if cleaned.isdigit():
        category_id = int(cleaned)
        return CATEGORY_BY_ID.get(category_id)

    exact_matches = []
    for category in CATEGORIES:
        possible_names = [category["title"], *category["aliases"]]
        normalized_names = {normalize(name) for name in possible_names}
        if cleaned in normalized_names:
            exact_matches.append(category)

    if len(exact_matches) == 1:
        return exact_matches[0]

    partial_matches = []
    for category in CATEGORIES:
        possible_names = [category["title"], *category["aliases"]]
        normalized_names = [normalize(name) for name in possible_names]
        if any(cleaned in name or name in cleaned for name in normalized_names):
            partial_matches.append(category)

    if len(partial_matches) == 1:
        return partial_matches[0]

    return None


def render_board(category: Dict, found_indices: Set[int]) -> str:
    lines = []
    for index, answer in enumerate(category["answers"], start=1):
        value = answer["name"] if index in found_indices else "....."
        lines.append(f"{index}. {value}")
    return "\n".join(lines)


def answer_matches(guess: str, answer: Dict) -> bool:
    guess_norm = normalize(guess)
    accepted = {normalize(answer["name"])}
    accepted.update(normalize(alias) for alias in answer.get("aliases", []))
    return guess_norm in accepted


def find_correct_answer(guess: str, category: Dict, found_indices: Set[int]) -> Optional[int]:
    for index, answer in enumerate(category["answers"], start=1):
        if index in found_indices:
            continue
        if answer_matches(guess, answer):
            return index
    return None


def is_already_found(guess: str, category: Dict, found_indices: Set[int]) -> bool:
    for index, answer in enumerate(category["answers"], start=1):
        if index not in found_indices:
            continue
        if answer_matches(guess, answer):
            return True
    return False


