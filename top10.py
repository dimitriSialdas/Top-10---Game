def choose_category() -> Dict:
    while True:
        print_categories()
        choice = input("Choose a category by number or by name: ").strip()
        category = category_lookup(choice)

        if category:
            return category

        print("I could not find that category. Please try again.\n")


def show_full_list_and_sources(category: Dict) -> None:
    print("\n" + "=" * 72)
    print(f"FULL LIST — {category['title'].upper()}")
    print("=" * 72)
    for index, answer in enumerate(category["answers"], start=1):
        print(f"{index}. {answer['name']}")

    print("\nSource note:")
    print(category["source_note"])

    print("\nSources:")
    for source in category["sources"]:
        print(f"- {source['title']}")
        print(f"  {source['url']}")
    print("=" * 72 + "\n")


def play_round(category: Dict) -> None:
    print("\n" + "=" * 72)
    print(f"CATEGORY: {category['title']}")
    print("=" * 72)
    print(f"Ranking basis: {category['ranking_note']}")
    print(f"Lives: {MAX_LIVES}")
    print()
    print(render_board(category, set()))

    lives = MAX_LIVES
    found_indices: Set[int] = set()
    first_turn = True

    while lives > 0 and len(found_indices) < 10:
        prompt = "\nGive me your first answer: " if first_turn else "\nNext answer: "
        guess = input(prompt).strip()

        if not guess:
            print("Please type an answer.")
            continue

        if normalize(guess) in {"quit", "exit"}:
            print("\nRound ended early.")
            break

        if is_already_found(guess, category, found_indices):
            print("You already found that answer. Try another one.")
            continue

        correct_index = find_correct_answer(guess, category, found_indices)

        if correct_index is not None:
            found_indices.add(correct_index)
            correct_name = category["answers"][correct_index - 1]["name"]
            print(f"\nCorrect! #{correct_index} is {correct_name}.")
        else:
            lives -= 1
            if lives > 0:
                print(f"\nWrong answer. Lives left: {lives}")
            else:
                print("\nWrong answer. That was your last life.")

        print()
        print(render_board(category, found_indices))
        first_turn = False

    if len(found_indices) == 10:
        print("\nAmazing! You found all 10 answers.")
    elif lives == 0:
        print("\nYou lost. Game over.")

    while True:
        print("\nWhat do you want to do now?")
        print("1. Play again")
        print("2. Show the full list + sources")
        print("3. Quit")
        after_round = input("Choose 1, 2, or 3: ").strip()

        if after_round == "1":
            print()
            return "again"

        if after_round == "2":
            show_full_list_and_sources(category)
            while True:
                next_step = input("Type 1 to play again or 2 to quit: ").strip()
                if next_step == "1":
                    print()
                    return "again"
                if next_step == "2":
                    print("Thanks for playing!")
                    return "quit"
                print("Please choose 1 or 2.")

        if after_round == "3":
            print("Thanks for playing!")
            return "quit"

        print("Please choose 1, 2, or 3.")


def main() -> None:
    print_intro()

    while True:
        category = choose_category()
        action = play_round(category)
        if action == "quit":
            break


